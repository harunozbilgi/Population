namespace animalpopulation.Entities.Rabbits;

public class Rabbit
{
    public int LifeTime;
    public int Age;
    public int getAge()
    {
        return Age;
    }
    public void setAge(int age)
    {
        Age = age;
    }
    public int getLifetime()
    {
        return LifeTime;
    }
    public void setLifetime(int lifetime)
    {
        LifeTime = lifetime;
    }
}
